pub struct LinearRegression {
    w: f64,
    b: f64,
}

impl LinearRegression {
    pub fn train(&mut self, alpha: f64, epochs: usize, xs: &[f64], ys: &[f64]) {
        for e in 0..epochs {
            self.update_w_and_b(alpha, xs, ys);

            // log progress every 400 iterations
            if e % 400 == 0 {
                println!("epoch: {}, loss: {}",
                    e,
                    self.avg_loss(xs,ys),
                );
            }
        }
    }

    pub fn predict(&self, x: f64) -> f64 {
        self.w * x + self.b
    }

    fn update_w_and_b(&mut self, alpha: f64, xs: &[f64], ys: &[f64]) {
        assert!(xs.len() == ys.len());
        // what is dl_dw?
        let mut dl_dw = 0.0;
        let mut dl_db = 0.0;

        #[allow(non_snake_case)]
        let N = xs.len();

        for i in 0..N {
            // show the partial derivatives here
            dl_dw += -2.0 * xs[i] * (ys[i] - (self.w * xs[i] + self.b));
            dl_db += -2.0 * (ys[i] - (self.w * xs[i] + self.b));
        }

        // update
        self.w = self.w - (1.0 / N as f64) * dl_dw * alpha;
        self.b = self.b - (1.0 / N as f64) * dl_db * alpha;
    }

    fn avg_loss(&mut self, xs: &[f64], ys: &[f64]) -> f64 {
        assert!(xs.len() == ys.len());

        #[allow(non_snake_case)]
        let N = xs.len();

        let mut total_error = 0.0;

        for i in 0..N {
            total_error += (ys[i] - (self.w * xs[i] + self.b)).powf(2.0)
        }

        total_error / N as f64
    }
}

